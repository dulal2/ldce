export default function ResultsPage({
  params,
  searchParams,
}: {
  params: { topic: string }
  searchParams: { score: string; answers: string }
}) {
  const topic = params.topic
  const score = Number.parseInt(searchParams.score, 10)
  const userAnswers = searchParams.answers.split(",").map(Number)
  const questions = quizData[topic as keyof typeof quizData]

  if (!questions) {
    return (
      <div className="min-h-screen bg-gray-100 py-12 px-4 sm:px-6 lg:px-8">
        <div className="max-w-3xl mx-auto text-center">
          <h1 className="text-3xl font-bold mb-8">Topic not found</h1>
          <Link
            href="/"
            className="bg-blue-500 hover:bg-blue-600 text-white font-bold py-2 px-4 rounded transition duration-300"
          >
            Back to Topics
          </Link>
        </div>
      </div>
    )
  }

  return (
    <div className="min-h-screen bg-gray-100 py-12 px-4 sm:px-6 lg:px-8">
      <div className="max-w-3xl mx-auto">
        <h1 className="text-3xl font-bold text-center mb-8 capitalize">{topic.replace(/_/g, " ")} Quiz Results</h1>
        <div className="bg-white shadow-md rounded-lg p-6 mb-8">
          <h2 className="text-2xl font-semibold text-center mb-4">
            Your Score: {score} / {questions.length}
          </h2>
          {questions.map((q, index) => (
            <div key={index} className="mb-4 p-4 border rounded">
              <p className="font-semibold mb-2">{q.question}</p>
              <div className="grid gap-2">
                {q.options.map((option, optionIndex) => (
                  <div
                    key={optionIndex}
                    className={`p-2 rounded ${
                      optionIndex === q.correctAnswer
                        ? "bg-green-100 text-green-800"
                        : optionIndex === userAnswers[index]
                          ? "bg-red-100 text-red-800"
                          : ""
                    }`}
                  >
                    {option}
                    {optionIndex === q.correctAnswer && (
                      <CheckCircle className="inline-block ml-2 h-5 w-5 text-green-600" />
                    )}
                    {optionIndex === userAnswers[index] && optionIndex !== q.correctAnswer && (
                      <XCircle className="inline-block ml-2 h-5 w-5 text-red-600" />
                    )}
                  </div>
                ))}
              </div>
            </div>
          ))}
        </div>
        <div className="text-center">
          <Link
            href="/"
            className="bg-blue-500 hover:bg-blue-600 text-white font-bold py-2 px-4 rounded transition duration-300"
          >
            Back to Topics
          </Link>
        </div>
      </div>
    </div>
  )
}

